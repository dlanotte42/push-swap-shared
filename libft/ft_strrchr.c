#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s) + 1;
	while (i > 0 && s[i - 1] != (char)c)
	{
		i--;
	}
	if (i > 0)
		return ((char *)(s + i - 1));
	return (0);
}
